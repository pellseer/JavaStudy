package operator;

public class OperatorEx3 {
  public static void main(String[] args) {

    int score = 100;

    System.out.println(++score); // 증가 연산자
    System.out.println(score++);
    System.out.println(score);
    System.out.println(--score); // 감소 연산자
    System.out.println(score--);
    System.out.println(score);

  }
}
